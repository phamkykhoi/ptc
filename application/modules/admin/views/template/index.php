	function save_template_file(){
		var tpl_file = $("#tpl_file").val() ;
		var folder = $("#tpl_folder").val() ;
		
		document.getElementById('save-button').className = "btn disabled";
		$("#save-button").attr("disabled", "true");
		//$("#bottom-btn").html("<img src='images/spinner.gif'>");
		
		if(tpl_file == 'add_script'){
			
			var f_name = $("#f_name").val();
			var tpl_code = $("#tpl_editor").val();
			var f_type = $("#f_type").val();
			
			$("#template-editor").html("<img src='/admin/images/spinner.gif'>");
			
			if(f_name.length > 0 && f_name.indexOf(' ') < 0 ){
				$.post("action.php", {
							action : "add-script-tpl"
							, f_name : f_name
							, f_type : f_type
							, tpl_code: tpl_code
						}, function(data) { 
							$("#template-editor").html( data );
						} );
			}else{
				alert('Tên file chưa có hoặc có dấu cách !');	
			}
		}else{
			var content = CodeMirror_editor.getValue();
			
			$("#template-editor").html("<img src='/admin/images/spinner.gif'>");
			
			$.post("action.php", {
						action : "update-tpl"
						, tpl_file : tpl_file
						, folder : folder
						, tpl_code: content //$("#tpl_editor").val()
					}, function(data) { 
						$("#template-editor").html( data );
					} );
		}
		
		//$("#saving_status").html("");
		document.getElementById('save-button').className = "btn";
		$("#save-button").removeAttr("disabled");
	}
	
